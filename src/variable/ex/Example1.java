package variable;

public class Example1 {
    public static void main(String[] args) {
        System.out.println("without variable");
        System.out.println(4 + 3);
        System.out.println(4 - 3);
        System.out.println(4 * 3);

        System.out.println("with variable");
        int four = 4;
        int three = 3;
        System.out.println(four + three);
        System.out.println(four - three);
        System.out.println(four * three);
    }
}
